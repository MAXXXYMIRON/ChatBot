namespace Chat.Words
{
    /// <summary>
    /// Класс с функциями возврата знаков препиания.
    /// </summary>
    static class Signs
    {
        /// <summary>
        /// Вставка в предложение знака - ". "
        /// </summary>
        static public string Dot() => ". ";
        /// <summary>
        /// Вставка в предложение знака - " "
        /// </summary>
        static public string Space() => " ";
        /// <summary>
        /// Вставка в предложение знака - "? "
        /// </summary>
        static public string QuestionMark() => "? ";
        /// <summary>
        /// Вставка в предложение знака - ", "
        /// </summary>
        static public string Comma() => ", ";
        /// <summary>
        /// Вставка в предложение знака - "! "
        /// </summary>
        static public string ExclamationMark() => "! ";
        /// <summary>
        /// Вставка в предложение знака - " - "
        /// </summary>
        static public string Line() => " - ";
    }
}
